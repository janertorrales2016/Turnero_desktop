    private void btnrecordarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrecordarActionPerformed
        DBconnection conex = new DBconnection();

        String valor = conex.recordarcontra(txtrecoruser.getText(), txtrecornombre.getText(), Integer.parseInt(txtrecordcaja.getText()));
        if ("error".equals(valor)) {
            JOptionPane.showMessageDialog(null, "Usuario, Nombre o caja incorrectos");
        } else {
            JOptionPane.showMessageDialog(null, "Tu contraseña es: " + valor + "\n puede modificar la contraseña desde la configuracion");
            this.rsnpanel.setPanelSlider(2, pnlLogin, rojeru_san.RSPanelsSlider.DIRECT.RIGHT);
        }

    }//GEN-LAST:event_btnrecordarActionPerformed

