    private void rSButtonEffect1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonEffect1ActionPerformed

        DBconnection conex = new DBconnection();

        String valor = conex.login(user.getText(), clave.getText());
        if ("2".equals(valor)) {
            JOptionPane.showMessageDialog(null, "usuario o contraseña incorrecta");
        } else {
            if ("1".equals(valor)) {
                Administrador abrir = new Administrador();
                abrir.setVisible(true);
                this.setVisible(false);
            } else {
                Menu abrirmenu = new Menu();
                abrirmenu.setVisible(true);
                this.setVisible(false);
            }

        }
    }//GEN-LAST:event_rSButtonEffect1ActionPerformed

